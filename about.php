	<strong>Social:</strong>  To provide programs for men and women to teach them how to become more responsible as parents, husbands, 
	wives, and citizens.  To sponsor and promote activities, which honor fathers who are responsible parents with an 
	aim toward encouraging other men in the community to be actively involved in the lives of their children, to pay 
	child support, to participate in the health and welfare of their children.  To provide mentors and role models for 
