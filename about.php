	          <p><img src="images/about_history.png" alt="History" width="109" height="57" border="0" /></p>
			  Judge Mablean Ephriam is a mother of four, grandmother of nine and was raised in South Central Los Angeles.  
			  She has been in the practice of law almost thirty years.  She presided as the Judge of the popular television 
			  series, Divorce Court, for seven years.  Based on her life and professional experiences, she is keenly aware 
			  of the alarming rate of illiteracy, the declining quality of our educational system, especially the inner-city 
			  schools, the lack of adequate and affordable housing,  the need for child care, the high rate of unemployment,  
			  the dissipation of the family and declining moral values.  She became fed-up with the increased rate of fatherless 
			  homes, due to imprisonment divorce, unwanted pregnancies, drug abuse and suicide, especially in African-American 
			  and Latino-Hispanic families.  She witnessed the widening gap between the haves and have-nots, the advantaged 
			  and disadvantaged.  Therefore, she decided to do something to address these conditions and hopefully change 
			  the circumstances.
  <P>
	In 2003 Judge Mablean decided to host a brunch and awards program to celebrate fathers. She believed that if we accentuated 
	the positive and de-emphasize the negative, more fathers would become involved in the lives of their children thus 
	strengthening families.  Within three months of that decision, she became the Founder and Host of the Honoring Unsung 
	fathers (H.U.F.) Awards and Brunch.  This inaugural brunch was held June 15, 2003 in San Pedro, California at the 
	Hilton Marina Hotel. Eight extraordinary fathers were personally selected by Judge Mablean to receive these inaugural 
	awards.  The Committee prepared for 250 people. Over 400 persons came. Some had to be turned away.  Due to the overwhelming 
	support for the event, she then decided to make the H.U.F. Awards and Brunch an annual affair.
	</P>  <P>
	Judge Mablean used her funds for the inaugural H.U.F. Awards and Brunch.  However, desiring to expand, she decided to 
	apply for non-profit status in order to seek corporate sponsorship and the financial support of other business leaders 
	who shared her concern for better communities.   The Foundation applied for its non-profit status in February, 2004 and 
	was granted the same on May 17, 2004.   Given the enthusiasm of the fathers who received the first awards and their 
	families, Judge Mablean decided to open the nominations to the public to allow mothers, wives, children, co-workers, 
	relatives and friends to nominate a father in their lives who deserved to be honored.   In 2004, over 75 applications 
	were received.  Nominations are now accepted each year in five standing categories:  Solo Warrior (single father), 
	Love Cares (married father), Fatherhood Forever (divorced father), Village Dad (non-biological father) and Living 
	Legacy (the elderly role-model father).</P>
