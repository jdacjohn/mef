		<h3><a name="JMDV">JUDGE MABLEAN ON DOMESTIC VIOLENCE</a></h3>


	<p>	<p>Judge Mablean has an undeniable passion for bringing awareness to domestic violence and providing preventive 
		solutions to reduce its prevalence.  As a victim of psychological and physical abuse in her marriage, 
		Judge Mablean has empathy for the appalling effects these actions have on victims.</p>

	<p>	During Judge Mablean’s tenure as Prosecuting Attorney for the City Attorney of Los Angeles, she reviewed thousands of cases involving crimes against women by men and frequently these cases were not prosecuted as the victim would request that the charges be dropped. Judge Mablean viewed this as her calling to utilize her influence in the court system to forever change how domestic violent charges were handled in the State of California.  
		</p>
	<p>	As part of a committee of Lawyers and Judges, she had an influence in drafting the legislation for what is currently known as the Domestic Violence Prevention Act for the State of California.  This Act convicts the defendant of a misdemeanor if found guilty and the victim could not contest the charges (is this correct, I could not find year this was implemented).  Judge Mablean remained adamant in discovering new solutions to the prevention and intervention of domestic violence.  She recommended to the Presiding Judge of the Municipal Court to implement a “diversion program” for the defendants, which would allow the accused to be relived of a conviction if they participate in counseling for a specified period of time and not be involved in any other acts of violence, particularly against the victim.
		</p>
	<p>	She made an impact at the grassroots level by co-founding the Family Law Project along with Pasty Ostroy -a colleague in 1982. The program was designed as a legal clinic to provide assistance to women in obtaining restraining orders against their abuser.     Judge Mablean dedicated countless hours of volunteer work to sustain the early stages of the organization. This organization continues to focus on prevention of domestic violence and protection against domestic violence under the new name Harriet  Buhai Center For Family Law, located in Los Angeles California.
		</p>
	<p>	Judge Mablean also assisted in the formation of the Jenessee Center for Domestic Violence, a domestic violence shelter for women.  She continues to be an avid supporter and contributor to the Jenessee Center.  Continually, she lectures throughout the nation at churches, schools and colleges on the effects, solutions and intervention strategies of domestic violence. 
		</p>
		As domestic violence continues to persist and its occurrences are increasing, Judge Mablean - a devoted steward against domestic violence- has welcomed the offer to tour the State of California sharing her experience and expertise on domestic violence.  Further, she views the participation of fathers and young men key to prevention measures. She will use her influence in Honoring Unsung Fathers to expand the audience to include both men and women.  
		<h3><a name="seminars">SEMINARS ON DOMESTIC VIOLENCE</a></h3>
	<p>Seminars are offered on Domestic Violence Prevention.   MEF works closely with the Jenessee Center, a local domestic violence shelter in Los Angeles, to educate the victims of domestic violence on prevention and to educate them on legal remedies available..   MEF works closely with STV America, another local non-profIt organization which offers Domestic Violence Counseling and Classes for domestic violence offenders in a joint effort to educate the offender and to prevent a reoccurrence of domestic violence acts.</p>
