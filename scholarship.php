<p style="width:600px;">This scholarship will be awarded to a current year high school graduate who will be attending Howard University in the fall. It is funded by Howard University Alumni who is a MEF board member.
</p>

<h3><a name="">THOMAS JEFFERSON HIGH SCHOOL CLASS OF 1967 SCHOLARSHIP</a></h3>

<p style="width:600px;">	Scholarships will be awarded to a current year Thomas Jefferson High School (1319 E. 41st St. Los Angeles, CA) graduate.  It is funded by the TJHS Class of 1967 Alumni of which the President/Founder is a member.
</p>
<span>SCHOLARSHIP ELIGIBILITY REQUIREMENTS</span><br><br>



In order to qualify for a scholarship from the MABLEAN EPHRIAM FOUNDATION, YOU MUST MEET THE FOLLOWING REQUIREMENTS:<br><br>

<span style="font-size: 14px; color: black; font-weight: bold; text-decoration: underline;">ALL APPLICANTS:</span><br><br>

1.	Annual Family Income must be at or below federal poverty guidelines.<br>
2.	Must reside in Los Angeles County..<br>
3.	Must complete a typed essay as set forth on the Application..<br>
4.	Must attend the Honoring Unsung Fathers Awards and Scholarship Brunch on Father's Day..<br>
5.	Must be a high school graduate or equivalent ( i.e., G.E.D., diploma)..<br>.<br>


<span style="font-size: 14px; color: black; font-weight: bold; text-decoration: underline;">CURRENT HIGH SCHOOL GRADUATES:</span><br>.<br>

1.	Must have an overall academic G.P.A. of 2.0 or better..<br>
2.	Must have been involved in some type of community activity or extra-curricula activity which allowed you to 
contribute your time, talent and or resources to serving others..<br><br>


<span style="font-size: 14px; color: black; font-weight: bold; text-decoration: underline;">APPLICANTS 20 TO 35 YEARS:</span><br><br>

1.	Must have had at least a 2.0 G. P. A. or better in high school..<br>
2.	If G.E.D. diploma, must have passed the G.E.D. examination..<br>
3.	If attending college, must have a G.P.A. of 2.0 or better the last academic year attended..<br>
4.	Must be employed now or if unemployed, no more than one (1) year, unless you are a stay-at-home 
parent with at least one minor child five (5) years old or under...<br>
5.	Must be involved in some type of community activity, group or organization which renders service to others.<br>

	
